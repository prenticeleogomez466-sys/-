# -*- coding: utf-8 -*-
"""创意杂交引擎:撞"题材簇 × 远域元素 × 机制家族"生成新开局候选,自动避开用滥家族。
用法: python idea_forge.py [数量=12] [seed=0]
新意来自远域相撞(法务流=律师×契约,bug猎手=程序员×规则)。输出供毒舌评委/人工筛。
"""
import sys, random

GENRES = ["都市异能","玄幻修真","仙侠","武侠","末世废土","科幻星际","无限流","游戏网游",
          "规则怪谈","历史争霸","谍战悬疑","克苏鲁诡异","铁血军事","职业手艺","种田经营",
          "古言宫斗(女频)","现言豪门(女频)","穿书快穿(女频)","玄幻女强(女频)"]

DOMAINS = ["程序员(找bug/提权/补丁)","律师(合同/举证/钻空子)","会计(资产负债/做账)",
           "医生(诊断/手术)","厨师(火候/配方)","刑警(侦讯/侧写)","保险精算(概率/赔付)",
           "考古(还原/断代)","谈判专家","债务催收","殡葬师","锁匠","调香师","电竞教练",
           "博弈论","热力学(熵)","经济学(套利/做空)","语言学(命名/能指)","生态学(食物链)",
           "密码学","神经科学(记忆/认知)","股市(杠杆/CLV)","拍卖/典当/租赁","信用评分",
           "二十四节气","古代刑名","堪舆星象","各地葬俗","35岁职场淘汰","彩礼/学区房焦虑"]

MECH = ["信息差(预知/读心/模拟器/面板透视)","时间(局部存档/回溯/时间债)","空间(随身界/锚点/折叠)",
        "概念规则(批注成真/删除键/概率拖拽/缺字)","身份(伪装/称号/马甲)","资源经济(万能仓库/复利/气运银行)",
        "契约交换(献祭/罪换力/愿力/借命/影子典当)","复制窃取(复刻/拓印/记忆/巅峰)",
        "具现养成(情绪化武/烹饪赋能/体内种田/图鉴削弱)","数值系统(属性/加点/结算)",
        "概率因果(概率编辑/必然律/因果债)","语言文字(朱批/契约笔/命名支配/绝对命令)",
        "感官知觉(透视/听谎/痛觉转移/预兆)","生命生态(进化/亡魂军团/血脉/同化)",
        "模拟推演(人生模拟/反派模拟/战局推演)","制造创造(蓝图/符器/合成/造神)",
        "精神心智(催眠/精神入侵/梦境/人格战力)","秩序收容(异常处置/封印/镇守换供奉)"]

USED = ["签到","抽奖","奖池","吞噬天赋","回收","熔炉","收破烂","轮回","活N遍","词条","打补丁",
        "改属性","借命","抄命","夺光环","抢气运","进化经验条","死亡兑换"]

def main():
    n = int(sys.argv[1]) if len(sys.argv) > 1 else 12
    seed = int(sys.argv[2]) if len(sys.argv) > 2 else 0
    rnd = random.Random(seed)
    combos = set()
    out = []
    tries = 0
    while len(out) < n and tries < n * 50:
        tries += 1
        g, d, m = rnd.choice(GENRES), rnd.choice(DOMAINS), rnd.choice(MECH)
        key = (g, d, m)
        if key in combos:
            continue
        combos.add(key)
        warn = " ⚠撞用滥家族" if any(u in m for u in USED) else ""
        out.append((g, d, m, warn))

    print(f"=== 创意杂交候选 ({len(out)} 个, seed={seed}) ===")
    print("公式: 题材簇 × 远域元素(现实骨架) × 机制家族 → 越远越新\n")
    for i, (g, d, m, warn) in enumerate(out, 1):
        print(f"{i:>2}. 【{g}】× 〔{d}〕× 「{m.split('(')[0]}」{warn}")
        print(f"    → 把『{d.split('(')[0]}』的底层逻辑做成『{m.split('(')[0]}』金手指,放进{g.split('(')[0]}世界。")
    print("\n下一步: 给每个候选按黄金配方补 身份×处境×反差+一句话开局,过 金手指天花板自检,再交毒舌评委。")

if __name__ == "__main__":
    main()
