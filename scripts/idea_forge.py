# -*- coding: utf-8 -*-
"""创意杂交引擎:撞"题材簇 × 远域元素 × 机制家族"生成高概念候选。
用法: python idea_forge.py [数量=12] [seed=0] [--wild]
  --wild: 远域元素改用"科学奇想/概念悖论/社会荒诞/神话冷门"等更野源头,新鲜度更高。
新意来自远域相撞;输出带"高概念打磨指引+化学反应自检",供打磨成眼前一亮,再交评委。
"""
import sys, random

GENRES = ["都市异能","玄幻修真","仙侠","武侠","末世废土","科幻星际","无限流","游戏网游",
          "规则怪谈","历史争霸","谍战悬疑","克苏鲁诡异","铁血军事","职业手艺","种田经营",
          "古言宫斗(女频)","现言豪门(女频)","穿书快穿(女频)","玄幻女强(女频)"]

DOMAINS = ["程序员(找bug/提权/补丁)","律师(合同/举证/钻空子)","会计(资产负债/做账)",
           "医生(诊断/手术)","厨师(火候/配方)","刑警(侦讯/侧写)","保险精算(概率/赔付)",
           "考古(还原/断代)","谈判专家","债务催收","殡葬师","锁匠","调香师","电竞教练",
           "博弈论","热力学(熵)","经济学(套利/做空)","语言学(命名/能指)","生态学(食物链)",
           "密码学","神经科学(记忆/认知)","股市(杠杆/CLV)","拍卖/典当/租赁","信用评分",
           "二十四节气","古代刑名","堪舆星象","各地葬俗","35岁职场淘汰","彩礼/学区房焦虑"]

MECH = ["信息差(预知/读心/模拟器/面板透视)","时间(局部存档/回溯/时间债)","空间(随身界/锚点/折叠)",
        "概念规则(批注成真/删除键/概率拖拽/缺字)","身份(伪装/称号/马甲)","资源经济(万能仓库/复利/气运银行)",
        "契约交换(献祭/罪换力/愿力/借命/影子典当)","复制窃取(复刻/拓印/记忆/巅峰)",
        "具现养成(情绪化武/烹饪赋能/体内种田/图鉴削弱)","数值系统(属性/加点/结算)",
        "概率因果(概率编辑/必然律/因果债)","语言文字(朱批/契约笔/命名支配/绝对命令)",
        "感官知觉(透视/听谎/痛觉转移/预兆)","生命生态(进化/亡魂军团/血脉/同化)",
        "模拟推演(人生模拟/反派模拟/战局推演)","制造创造(蓝图/符器/合成/造神)",
        "精神心智(催眠/精神入侵/梦境/人格战力)","秩序收容(异常处置/封印/镇守换供奉)"]

# --wild: 比"职业×学科"更野的创意源头,新鲜度更高
WILD = ["熵增(让一切加速腐朽)","量子叠加(没观测前生死未定)","进化论(逆向退化/定向变异)",
        "信息论(把人压缩成一句话)","博弈论(纳什均衡锁死)","忒修斯之船(换到第几块零件你还是你)",
        "电车难题(被迫五选一的规则)","祖父悖论(杀因得果)","罗素悖论(自指即崩坏)",
        "信用分极端化(分低到不准呼吸)","KPI极端化(完不成绩效就消失)","算法推荐(被茧房圈死)",
        "学区房/相亲市场极端化","赶尸/海客/傩戏(冷门民俗)","山海经边角异兽","各地葬俗禁忌",
        "临终关怀的潜规则","医疗事故鉴定","破产清算/资产重组","直播打赏(气运打赏)",
        "二手交易(转卖命运)","盲盒/扭蛋(开出人生)","保修/退换货(人生可退货)","订阅制(按月续命)"]

USED = ["签到","抽奖","奖池","吞噬天赋","回收","熔炉","收破烂","轮回","活N遍","词条","打补丁",
        "改属性","借命","抄命","夺光环","抢气运","进化经验条","死亡兑换"]

def main():
    args = [a for a in sys.argv[1:] if not a.startswith("--")]
    wild = "--wild" in sys.argv
    n = int(args[0]) if len(args) > 0 else 12
    seed = int(args[1]) if len(args) > 1 else 0
    rnd = random.Random(seed)
    pool = WILD if wild else DOMAINS
    combos = set()
    out = []
    tries = 0
    while len(out) < n and tries < n * 50:
        tries += 1
        g, d, m = rnd.choice(GENRES), rnd.choice(pool), rnd.choice(MECH)
        key = (g, d, m)
        if key in combos:
            continue
        combos.add(key)
        warn = " ⚠撞用滥家族,换机制" if any(u in m for u in USED) else ""
        out.append((g, d, m, warn))

    src = "野源(科学奇想/悖论/社会荒诞/民俗)" if wild else "远域元素(现实职业/学科)"
    print(f"=== 创意杂交候选 ({len(out)} 个, seed={seed}, 源={src}) ===")
    print("流程: 撞原料 → 打磨成高概念(一句话+化学反应) → 过眼前一亮五维 → 交评委\n")
    for i, (g, d, m, warn) in enumerate(out, 1):
        gg, dd, mm = g.split('(')[0], d.split('(')[0], m.split('(')[0]
        print(f"{i:>2}. 【{gg}】× 〔{dd}〕× 「{mm}」{warn}")
        print(f"    打磨指引: 把『{dd}』的底层逻辑做成『{mm}』金手指,放进{gg}世界。")
        print(f"    → 高概念待填:「当{dd}变成一种能力,一个低位者靠它______(一句话讲清+勾人)」")
        print(f"    → 化学反应自检: 这金手指换到别的题材还成立吗? 成立=硬凑淘汰; 只在{gg}才爆=融合,留。")
    print("\n眼前一亮五维: ①搜不到同款 ②一句话讲清 ③我也想要 ④有忘不掉的画面 ⑤金手指×题材融合非硬凑。")
    print("低于线的丢; 过线的按黄金配方补 身份×处境×反差+一句话开局,再交毒舌评委。")

if __name__ == "__main__":
    main()
